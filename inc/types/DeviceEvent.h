#ifndef DEVICE_EVENT_H
#define DEVICE_EVENT_H

#include "DeviceConfig.h"

// Wildcard event codes
#define DEVICE_ID_ANY         0
#define DEVICE_EVT_ANY        0

namespace codal
{
    enum DeviceEventLaunchMode
    {
        CREATE_ONLY,
        CREATE_AND_FIRE
    };

    #define DEVICE_EVENT_DEFAULT_LAUNCH_MODE     CREATE_AND_FIRE

    /**
      * Class definition for a DeviceEvent
      *
      * It represents a common event that is generated by the various components on the codal device.
      */
    class DeviceEvent
    {
        public:

        uint16_t source;         // ID of the Device Component that generated the event e.g. DEVICE_ID_BUTTON_A.
        uint16_t value;          // Component specific code indicating the cause of the event.
        uint64_t timestamp;      // Time at which the event was generated. us since power on.

        /**
          * Constructor.
          *
          * @param src The id of the Device Component that generated the event e.g. DEVICE_ID_BUTTON_A.
          *
          * @param value A component specific code indicating the cause of the event.
          *
          * @param mode Optional definition of how the event should be processed after construction (if at all):
          *                 CREATE_ONLY: DeviceEvent is initialised, and no further processing takes place.
          *                 CREATE_AND_FIRE: DeviceEvent is initialised, and its event handlers are immediately fired (not suitable for use in interrupts!).
          *
          * @code
          * // Create and launch an event using the default configuration
          * DeviceEvent evt(id,DEVICE_BUTTON_EVT_CLICK);
          *
          * // Create an event only, do not fire onto an EventModel.
          * DeviceEvent evt(id,DEVICE_BUTTON_EVT_CLICK,CREATE_AND_FIRE);
          * @endcode
          */
        DeviceEvent(uint16_t source, uint16_t value, DeviceEventLaunchMode mode = DEVICE_EVENT_DEFAULT_LAUNCH_MODE);

        /**
          * Default constructor - initialises all values, and sets timestamp to the current time.
          */
        DeviceEvent();

        /**
          * Fires this DeviceEvent onto the Default EventModel, or a custom one!
          */
        void fire();
    };

    /**
      * Enclosing class to hold a chain of events.
      */
    struct DeviceEventQueueItem
    {
        DeviceEvent evt;
        DeviceEventQueueItem *next;

        /**
          * Constructor.
          * Create a new DeviceEventQueueItem.
          *
          * @param evt The event to be queued.
          */
        DeviceEventQueueItem(DeviceEvent evt);
    };
}

#endif
